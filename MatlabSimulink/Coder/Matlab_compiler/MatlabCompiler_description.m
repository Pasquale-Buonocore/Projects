MATLAB Compiler™ enables you to share MATLAB® programs as standalone applications and web apps.
With MATLAB Compiler you can also package and deploy MATLAB programs as MapReduce and Spark™ big data
applications and as Microsoft® Excel® Add-ins. End users can run your applications royalty-free using
MATLAB Runtime.

To provide browser-based access to your MATLAB web apps, you can host them using the development version
of MATLAB Web App Server™ included with MATLAB Compiler. MATLAB programs can be packaged into software
components for integration with other programming languages (with MATLAB Compiler SDK™). 
Large-scale deployment to enterprise systems is supported through MATLAB Production Server™.

To generate C and C++ source code from MATLAB, use MATLAB Coder™.
